package com.sagar.proteinfinder;

public class FoodBean {

	public String title;
	public String qserv;
	public String prot;
	public String cal;
	public String pperc;
	public int draw;
	private int type;
	
	public FoodBean()
	{
		
	}
	
	
	
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getQserv() {
		return qserv;
	}
	public void setQserv(String qserv) {
		this.qserv = qserv;
	}
	public String getProt() {
		return prot;
	}
	public void setProt(String prot) {
		this.prot = prot;
	}
	public String getCal() {
		return cal;
	}
	public void setCal(String cal) {
		this.cal = cal;
	}
	public String getPperc() {
		return pperc;
	}
	public void setPperc(String pperc) {
		this.pperc = pperc;
	}
	public int getDraw() {
		return draw;
	}
	public void setDraw(int draw) {
		this.draw = draw;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	
	
	
	
}
